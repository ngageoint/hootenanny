geos::geom::Envelope TaskGrid::getBounds() const
{
  geos::geom::Envelope bounds;
  for (QList<TaskGridCell>::const_iterator cellItr = _cells.begin(); cellItr != _cells.end();
       ++cellItr)
  {
    bounds.expandToInclude(&(*cellItr).bounds);
  }
  return bounds;
}

